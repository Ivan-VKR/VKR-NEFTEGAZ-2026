def get_available_volumes() -> List[int]:
    return sorted(TANK_CATALOG.keys())


def get_tank_template(nominal_volume_m3: int) -> TankTemplate:
    if nominal_volume_m3 not in TANK_CATALOG:
        raise KeyError(f"Отсутствует типоразмер {nominal_volume_m3} м³")
    return TANK_CATALOG[nominal_volume_m3]
