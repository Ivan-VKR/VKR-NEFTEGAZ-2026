"""Справочные данные по типовым резервуарам.

Источник логики:
- ГОСТ 31385-2023: общая логика типовых вертикальных стальных резервуаров;
- ниже приведены УПРОЩЕННЫЕ демонстрационные типоразмеры для ВКР.

Важно:
- пользователь НЕ выбирает заранее РВС/РВСП/РВСПК;
- сначала выбирается номинальный объем, а затем программа сама
  использует типовой вертикальный стальной резервуар как базовую конструкцию
  и отдельно формирует рекомендацию по варианту:
  без понтона / с внутренним понтоном / с плавающей крышей.
"""

from dataclasses import dataclass
from typing import Dict, List


@dataclass(frozen=True)
class TankTemplate:
    tank_type: str
    nominal_volume_m3: int
    diameter_m: float
    shell_height_m: float
    roof_type: str
    shell_courses: int
    fill_factor: float
    note: str


